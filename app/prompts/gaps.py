ANALYSIS_PROMPT = """\
Проанализируй спецификацию фичи и найди пробелы в бизнес-логике. Выдай только то, \
за что готов отвечать перед командой. Чем меньше gap-ов — тем лучше, если спецификация качественная.

## Что является gap-ом (по приоритету)

### ПРИОРИТЕТ 1 — Недоопределённые взаимодействия
Спецификация ссылается на вызов внешнего сервиса, запись в таблицу или оповещение, \
но НЕ описывает детально: какие поля передавать, откуда брать значения, какой формат. \
Без этого разработчик НЕ СМОЖЕТ написать вызов — это самый блокирующий тип gap-а. \
Ищи формулировки вроде "оповестить сервис X", "вызвать метод Y", "записать в таблицу Z" \
без полного маппинга полей.

### ПРИОРИТЕТ 2 — Бизнес-логика
- Пропущенная ветка обработки: описан happy path, но не описано что делать при определённом статусе/условии
- Противоречие между шагами логики или между логикой и бизнес-правилами
- Поле используется в логике, но отсутствует в схеме зависимости (таблицы, API) — разработчик не сможет написать запрос
- Опциональное поле используется далее без указания что делать при его отсутствии, и это приведёт к неправильному бизнес-результату
- Неоднозначная формулировка, блокирующая реализацию: два разработчика прочитают по-разному
- Не описана бизнес-реакция на ошибочный ответ внешнего API: что делать с уже обновлёнными данными, \
какой статус выставить, что вернуть клиенту

## Что НЕ является gap-ом

- Конкретные retry/backoff/timeout стратегии — это решает разработчик
- Транзакционность, offset-менеджмент, идемпотентность — это архитектурные решения
- "Не описана обработка 5xx" без конкретного бизнес-последствия — это общее место
- Вопросы, ответ на которые очевиден из контекста спецификации
- Предложения "добавить логирование", "добавить мониторинг" — это не бизнес-логика

## Severity
- **critical** — разработчик не сможет реализовать шаг или реализует неправильно: потеря данных, \
неконсистентность, неправильный бизнес-результат
- **major** — проблема в не-основном сценарии, но приведёт к реальному багу в продакшене

## Правила
- Не более 10 gap-ов. Но чем меньше — тем лучше. Качество > количество.
- gap_type: свободный текст в snake_case, описывающий суть проблемы (например: `missing_branch`, \
`field_not_in_schema`, `contradictory_logic`, `ambiguous_condition`, `nullable_without_handling`)
- suggestion: конкретное требование для спецификации — ЧТО добавить/изменить, ГДЕ в логике. \
НЕ предлагай конкретные технические решения (retry, backoff, timeout значения).
- actionable: true если из suggestion разработчик может написать код; false если нужно уточнение у аналитика.
- Сортируй: critical первыми, затем major.
"""

